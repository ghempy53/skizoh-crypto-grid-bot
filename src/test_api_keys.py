# Your API credentials
api_key = 'paste_your_api_key_here'
api_secret = 'paste_your_secret_key_here'

# Initialize Binance.US connection
try:
    exchange = ccxt.binanceus({
        'apiKey': api_key,
        'secret': api_secret,
        'enableRateLimit': True
    })
    
    print("Testing connection to Binance.US...")
    
    # Test 1: Fetch balance
    balance = exchange.fetch_balance()
    print("‚úÖ API Connection Successful!\n")
    
    # Display balances
    print("Your Balances:")
    print(f"  USDT: {balance['USDT']['free']:.2f}")
    
    if balance['ETH']['free'] > 0:
        print(f"  ETH: {balance['ETH']['free']:.6f}")
    
    # Test 2: Fetch current ETH price
    ticker = exchange.fetch_ticker('ETH/USDT')
    print(f"\nCurrent ETH Price: ${ticker['last']:.2f}")
    
    # Test 3: Check minimum order requirements
    market = exchange.market('ETH/USDT')
    print(f"\nMinimum Order Size: {market['limits']['cost']['min']} USDT")
    
    print("\nüéâ All tests passed! Your API is ready to use.")
    
except ccxt.AuthenticationError as e:
    print(f"‚ùå Authentication Failed: {e}")
    print("Check that your API key and secret are correct.")
    
except ccxt.NetworkError as e:
    print(f"‚ùå Network Error: {e}")
    print("Check your internet connection.")
    
